fruits = {"ვაშლი", "ბანანი", "მანგო", "ატამი"}


fruits.add("ყურძენი")
print("დამატების შემდეგ:", fruits)


fruits.remove("ბანანი")
print("ბანანის წაშლის შემდეგ:", fruits)


print("ხილების რაოდენობა:", len(fruits))


fruits.pop()
print("pop()-ის შემდეგ:", fruits)


fruits.clear()
print("clear()-ის შემდეგ:", fruits)
